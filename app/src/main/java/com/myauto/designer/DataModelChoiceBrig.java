public class DataModelChoiceBrig {
    String name,cod;

    public DataModelChoiceBrig(String name, String cod) {
        this.name = name;
        this.cod = cod;
    }

    public String getName() {
        return name;
    }

    public String getCod() {
        return cod;
    }

}
