public class DataModelPush {

    String textPush;
    String dataPush;


    public DataModelPush(String textPush, String dataPush) {
        this.textPush = textPush;
        this.dataPush = dataPush;
    }

    public String getTextPush() {
        return textPush;
    }

    public String getDataPush() {
        return dataPush;
    }
}